static unsigned char *buf;

static void clear(void) {
    for (int i = 0; i < W*H*3; i += 3) {
        buf[i] = BG_R; buf[i+1] = BG_G; buf[i+2] = BG_B;
    }
}

static void fill_rect(int x, int y, int w, int h, int r, int g, int b) {
    for (int dy = 0; dy < h; dy++) {
        int py = y + dy;
        if (py < 0 || py >= H) continue;
        for (int dx = 0; dx < w; dx++) {
            int px = x + dx;
            if (px < 0 || px >= W) continue;
            int idx = (py * W + px) * 3;
            buf[idx] = r; buf[idx+1] = g; buf[idx+2] = b;
        }
    }
}

static void blend_pixel(int px, int py, int r, int g, int b, float a) {
    if (px < 0 || px >= W || py < 0 || py >= H) return;
    int idx = (py * W + px) * 3;
    buf[idx]   = (int)(buf[idx]   * (1-a) + r * a);
    buf[idx+1] = (int)(buf[idx+1] * (1-a) + g * a);
    buf[idx+2] = (int)(buf[idx+2] * (1-a) + b * a);
}

static void draw_char(int x, int y, char ch, int scale, int r, int g, int b, float alpha) {
    int idx = ch - 32;
    if (idx < 0 || idx > 94) return;
    for (int row = 0; row < 16; row++) {
        unsigned char bits = font_data[idx][row];
        for (int col = 0; col < 8; col++) {
            if (bits & (1 << col)) {
                for (int sy = 0; sy < scale; sy++)
                    for (int sx = 0; sx < scale; sx++)
                        blend_pixel(x + col*scale + sx, y + row*scale + sy, r, g, b, alpha);
            }
        }
    }
}

static int text_width(const char *s, int scale) {
    return (int)strlen(s) * 8 * scale;
}

static void draw_text(int x, int y, const char *s, int scale, int r, int g, int b, float alpha) {
    for (int i = 0; s[i]; i++) {
        draw_char(x + i * 8 * scale, y, s[i], scale, r, g, b, alpha);
    }
}

static void draw_text_centered(int y, const char *s, int scale, int r, int g, int b, float alpha) {
    int tw = text_width(s, scale);
    draw_text((W - tw) / 2, y, s, scale, r, g, b, alpha);
}

static void draw_text_gradient(int x, int y, const char *s, int scale,
    int r1, int g1, int b1, int r2, int g2, int b2, float alpha) {
    int len = (int)strlen(s);
    for (int i = 0; i < len; i++) {
        float t = len > 1 ? (float)i / (len - 1) : 0;
        int r = r1 + (int)((r2 - r1) * t);
        int g = g1 + (int)((g2 - g1) * t);
        int b = b1 + (int)((b2 - b1) * t);
        draw_char(x + i * 8 * scale, y, s[i], scale, r, g, b, alpha);
    }
}

static void draw_text_centered_gradient(int y, const char *s, int scale,
    int r1, int g1, int b1, int r2, int g2, int b2, float alpha) {
    int tw = text_width(s, scale);
    draw_text_gradient((W - tw) / 2, y, s, scale, r1, g1, b1, r2, g2, b2, alpha);
}

static void fade_frame(float alpha) {
    for (int i = 0; i < W*H*3; i++) {
        buf[i] = (int)(buf[i] * alpha);
    }
}

static float ease_out(float t) {
    return 1.0f - powf(1.0f - t, 3.0f);
}

static float ease_in_out(float t) {
    return t < 0.5f ? 4*t*t*t : 1 - powf(-2*t+2, 3)/2;
}

static float clampf(float v, float lo, float hi) {
    return v < lo ? lo : v > hi ? hi : v;
}

static void draw_bar(int x, int y, int max_w, int h, float fill, int r, int g, int b) {
    fill_rect(x, y, max_w, h, 20, 24, 35);
    int fw = (int)(max_w * fill);
    if (fw > 0) fill_rect(x, y, fw, h, r, g, b);
}

static void draw_bar_gradient(int x, int y, int max_w, int h, float fill,
    int r1, int g1, int b1, int r2, int g2, int b2) {
    fill_rect(x, y, max_w, h, 20, 24, 35);
    int fw = (int)(max_w * fill);
    for (int dx = 0; dx < fw; dx++) {
        float t = fw > 1 ? (float)dx / (fw - 1) : 0;
        int r = r1 + (int)((r2 - r1) * t);
        int g = g1 + (int)((g2 - g1) * t);
        int b = b1 + (int)((b2 - b1) * t);
        for (int dy = 0; dy < h; dy++)
            blend_pixel(x + dx, y + dy, r, g, b, 1.0f);
    }
}

// typewriter: returns how many chars to show based on progress
static int typewriter(const char *s, float t) {
    int len = (int)strlen(s);
    return (int)(len * clampf(t, 0, 1));
}

static void draw_typed(int x, int y, const char *s, float t, int scale, int r, int g, int b) {
    int n = typewriter(s, t);
    char tmp[512];
    if (n > 511) n = 511;
    memcpy(tmp, s, n);
    tmp[n] = 0;
    draw_text(x, y, tmp, scale, r, g, b, 1.0f);
    // cursor blink
    if (n < (int)strlen(s)) {
        int cx = x + n * 8 * scale;
        int frame_mod = (int)(t * 60) % 30;
        if (frame_mod < 20)
            fill_rect(cx, y, 2*scale, 16*scale, r, g, b);
    }
}

static void draw_typed_centered(int y, const char *s, float t, int scale, int r, int g, int b) {
    int tw = text_width(s, scale);
    draw_typed((W - tw) / 2, y, s, t, scale, r, g, b);
}

