// --- TRANSITIONS ---
static void apply_scene_fade(int f, int scene_start, int scene_end) {
    // Fade in first 15 frames, fade out last 15
    int fade_len = 15;
    if (f < scene_start + fade_len) {
        float a = (float)(f - scene_start) / fade_len;
        fade_frame(a);
    }
    if (f > scene_end - fade_len) {
        float a = (float)(scene_end - f) / fade_len;
        fade_frame(a);
    }
}

